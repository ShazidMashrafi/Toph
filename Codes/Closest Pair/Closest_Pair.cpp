void solve()
{
    int n;
    cin >> n;
    vector<pair<int,int>>v(n);
    for (auto &&i : v)
    {
        cin >> i.ff >> i.ss;
    }
    double mn = DBL_MAX;
    for (int i = 0; i < n; i++)
    {
        for (int j = i + 1; j < n; j++)
        {
            double d = sqrt(pow(v[i].ff - v[j].ff, 2) + pow(v[i].ss - v[j].ss, 2));
            // dbg(v[i], v[j], d);
            mn = min(mn, d);
        }
    }
    cout << fixed << setprecision(14) << mn << endl;
}

